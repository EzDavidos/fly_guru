// Классы для нативных полей выбора даты и времени.
//
// У <input type="date"> и <input type="time"> браузер рисует своё содержимое
// (07/23/2026, 12:00 PM — формат зависит от локали телефона), и на этом ломаются
// две вещи сразу:
//  • ячейка грида по умолчанию не ужимается ниже содержимого (min-width: auto),
//    поэтому поле распирает свою колонку и наезжает на соседнюю;
//  • Safari на iOS держит у такого поля «естественную» ширину и просто
//    игнорирует width: 100% — пока с него не снять системное оформление.
//
// Отсюда набор ниже. Префиксную запись оставляем ради старых iOS, где
// безпрефиксный appearance ещё не работал.
//
// Ставить рядом с обычными классами поля: `${NATIVE_PICKER} ${inputClass}`.
// Ячейке грида, в которой лежит такое поле, тоже нужен min-w-0.
export const NATIVE_PICKER = "min-w-0 appearance-none [-webkit-appearance:none]";
