import { defineRouting } from "next-intl/routing";

// Единое место конфигурации языков.
// localePrefix: "as-needed" — у дефолтного языка (ru) URL без префикса (/training),
// у остальных языков префикс добавлялся бы (/en/training, /vi/training).
//
// Пока сайт только на русском. Английский и вьетнамский из списка убраны
// намеренно: тексты страниц захардкожены по-русски, и с ними в списке гость с
// англоязычным браузером автоматически уезжал на /en и видел там русскую
// страницу — то есть язык вроде бы есть, а перевода нет. Файлы messages/en.json
// и messages/vi.json оставлены в репозитории: когда появится перевод, языки
// возвращаются одной строкой здесь.
export const routing = defineRouting({
  locales: ["ru"],
  defaultLocale: "ru",
  localePrefix: "as-needed",
});
