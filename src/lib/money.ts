// Разбор денежных сумм из форм. Жил внутри admin/actions.ts, но с паком A
// расходы вносит ещё и инструктор — а из файла с "use server" обычную функцию
// не экспортировать (там разрешены только асинхронные экшены). Вынесено сюда
// без изменений в поведении.

// Люди пишут суммы по-разному: «1500000», «1 500 000», «1.500.000», «1,500,000».
// Разделители принимаем только там, где они разбивают ровно по три цифры.
// Раньше разделители выкидывались без разбора, и «1.5» (в смысле «полтора
// миллиона») молча становилось чеком в 15 ₫ — без единой ошибки на экране.
// В донгах дробей не бывает: не угадываем, а возвращаем null, и вызывающий
// показывает «Сумма — число в донгах».
export function parseVnd(raw: FormDataEntryValue | null): number | null {
  const s = String(raw ?? "").replace(/\s/g, "");
  if (!s) return null;
  if (/^\d+$/.test(s)) return Number(s);
  if (/^\d{1,3}([.,]\d{3})+$/.test(s)) return Number(s.replace(/[.,]/g, ""));
  return null;
}
