-- ============================================================================
-- FlyGuru — Этап 2: учёт переходов по реф-ссылкам (таблица ref_visits)
-- Источник правды: docs/flyguru_architecture.md.
--
-- Зачем нужна: каждый заход на страницу /r/[код] мы записываем сюда одной
-- строкой. Это позволяет позже увидеть, сколько людей открыли ссылку агента,
-- даже если они не оставили заявку. Заявки лежат в bookings — это про другое
-- (про переходы, а не про записи).
--
-- Только additive: создаём новую таблицу, ничего существующего не трогаем.
-- ============================================================================

create table if not exists ref_visits (
  id         uuid primary key default gen_random_uuid(),
  code       text not null,                          -- реф-код из ссылки /r/<code>
  user_agent text,                                   -- строка браузера (для аналитики/антифрода)
  created_at timestamptz not null default now()      -- когда произошёл заход
);

-- Индекс по коду: чтобы быстро считать «сколько переходов у кода X».
create index if not exists ref_visits_code_idx on ref_visits (code);

-- RLS включаем для единообразия с остальными таблицами (0001). Политик не
-- добавляем — значит доступ есть только у service_role (наш сервер). Из браузера
-- сюда не пишут напрямую: запись идёт через серверный API-роут. Роли/политики —
-- Этап 3, здесь их намеренно не трогаем.
alter table ref_visits enable row level security;
