-- 1. «Путешествие» как услуга не существует — деактивируем (не удаляем:
--    на запись могут ссылаться существующие bookings).
-- 2. Экскурсия — теперь «Экскурсия на остров Черепахи» (вдвоём — по 3 млн ₫
--    с человека, примечание живёт в контенте сайта, в БД цена базовая 3,5 млн).
-- 3. Сафари — длительность 5–6 часов, а не «целый день» (в БД duration_min
--    и так NULL, меняем только название).
--
-- Идемпотентно: повторный прогон ничего не ломает.
-- ============================================================================

update services
set active = false
where name = 'Путешествие (день, несколько локаций)';

update services
set name = 'Экскурсия на остров Черепахи'
where name = 'Экскурсия вдоль побережья';

update services
set name = 'Сафари'
where name = 'Сафари (целый день)';
