-- ============================================================================
-- FlyGuru — Этап 4.6: аккаунты членов клуба.
--
-- Зачем: инвайт-ссылка /invite/<token> создаёт члену клуба auth-аккаунт
-- (строку в users с role=member). Но кабинету члена (Этап 5) нужно знать,
-- КАКОЙ клиент за этим аккаунтом стоит — его абонементы, сессии и награды
-- лежат в таблицах с client_id. Связь кладём в memberships.user_id —
-- зеркально паттерну агентов (agents.user_id → users).
--
-- Только additive: одна nullable-колонка, ничего существующего не трогаем.
-- unique: один аккаунт — одно членство (и наоборот).
-- ============================================================================

alter table memberships
  add column if not exists user_id uuid unique references users(id) on delete set null;
