-- 0010: связка строк services с контентом сайта.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0009).
--
-- Публичные страницы (обучение, тандем, прайс, клуб) берут цены и длительность
-- из базы по этому коду — правка цены в админке сразу меняет сайт. Тексты и
-- описания остаются в src/content/services.ts. Услуга без кода (созданная
-- админом позже) живёт только в CRM и на сайт не попадает.

alter table services add column if not exists code text unique;

-- Бэкфилл по названиям из seed.sql (с учётом переименований миграции 0004).
update services set code = v.code
from (
  values
    ('Базовое обучение (взрослый)',              'basic-adult'),
    ('Базовое обучение (до 14 лет)',             'basic-kid'),
    ('Базовое обучение вдвоём',                  'basic-duo'),
    ('Тандем с инструктором (взрослый)',         'tandem-adult'),
    ('Тандем с инструктором (до 14 лет)',        'tandem-kid'),
    ('Экскурсия на остров Черепахи',             'excursion'),
    ('Сафари',                                   'safari'),
    ('Самостоятельный прокат (после обучения)',  'rental'),
    ('Абонемент',                                'subscription'),
    ('Видеосъёмка полёта (Insta360, монтаж)',    'video')
) as v(name, code)
where services.name = v.name and services.code is null;
