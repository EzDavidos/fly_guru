-- 0011: город клиента + личная реф-ссылка инструктора.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0010).
--
-- Пачка правок №3, пак C:
--  c1) clients.city — город клиента (свободный текст), заводится в форме записи
--      и правится в карточке клиента админом. Только справочно.
--  c2) users.ref_code — личный код инструктора для ссылки /r/<код>. В отличие
--      от агентского кода (таблица agents) НЕ даёт скидку и реф-награду —
--      просто помечает заявку «пришёл к этому инструктору». Уникален среди
--      инструкторов; коллизии с кодами агентов маловероятны и разрешаются
--      в пользу агента при резолве (сперва ищем в agents, потом в users).

alter table clients add column if not exists city text;

alter table users add column if not exists ref_code text unique;
