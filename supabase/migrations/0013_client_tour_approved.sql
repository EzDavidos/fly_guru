-- 0013: допуск клиента к выездам (экскурсия/сафари) по решению инструктора.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0012).
--
-- Пачка правок №3, пак G: выезды больше не «только для членов клуба» — их
-- проводят ПО ОДОБРЕНИЮ ИНСТРУКТОРА, и уже уверенно катающему клиенту не нужен
-- абонемент. Признак ручной: инструктор/админ ставит его, когда считает клиента
-- готовым. Жёсткого блока в записи нет — это учёт и подсказка.
--
-- RLS не меняем: у инструктора нет update-политики на clients (только select +
-- insert), поэтому его тумблер идёт через service-role после проверки роли
-- (как загрузка аватара / создание агентов). Так не расширяем инструктору
-- запись на все поля клиента.

alter table clients add column if not exists tour_approved boolean not null default false;
