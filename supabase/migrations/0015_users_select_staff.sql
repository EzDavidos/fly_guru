-- 0015: персонал видит строки персонала — пак H2.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0014).
--
-- Зачем. Политика users_select_own (0005) пускает инструктора только к СВОЕЙ
-- строке users. Из-за этого:
--   • ЗП: доля абонементного котла = 10% продаж инструкторов ПОРОВНУ на всех
--     инструкторов, а посчитать делитель (сколько нас) инструктор не мог;
--   • календарь (H1): чужие смены рисовались как «?» — имя коллеги в join
--     не читалось.
-- Даём персоналу читать строки персонала. Клиенты/агенты/члены клуба
-- по-прежнему видят только себя: политика требует обе роли — и читающего,
-- и читаемого.

create policy users_select_staff on users
  for select to authenticated
  using (app_role() in ('instructor', 'admin') and role in ('instructor', 'admin'));
