-- 0017: контакты и фото клиента.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0016).
--
-- Пачка правок №4, пак B (пункты 7, 8):
--  • #8 — телефон клиента это в первую очередь мессенджер (WhatsApp/Telegram),
--    плюс необязательный ник в телеге: по нику пишут, когда номер не отвечает.
--  • #7 — фото клиента: админ просил возможность вспомнить, как человек
--    выглядит (имена в базе повторяются, а лиц он не помнит).

alter table clients add column if not exists telegram_username text;
alter table clients add column if not exists photo_url text;

-- ── Бакет для фото клиентов ──────────────────────────────────────────────────
-- Публичный, как avatars: ссылка попадает в next/image, а угадать путь по
-- uuid клиента снаружи нельзя. Политик записи намеренно НЕТ — загрузка идёт
-- только через сервер под service_role после проверки роли (тот же приём, что
-- у аватаров инструкторов и тумблера tour_approved: не расширяем инструктору
-- права на запись во все поля клиента).
insert into storage.buckets (id, name, public)
values ('clients', 'clients', true)
on conflict (id) do nothing;
