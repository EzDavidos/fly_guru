-- 0018: ник в Telegram у заявки с сайта.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0017).
--
-- Пачка правок №4, пак B (пункт 8, вторая половина).
--
-- Поле «Телефон ИЛИ ник в мессенджере» на сайте принимало и «+84…», и
-- «@username» в одну колонку phone. Из-за этого в базе оседали заявки, по
-- которым нельзя позвонить, а отличить их от настоящих номеров можно было
-- только глазами. Теперь на сайте два раздельных поля: телефон (обязательный,
-- с проверкой) и ник (необязательный) — и нику нужна своя колонка, иначе он
-- снова попал бы в phone.
--
-- При оформлении заявки в занятие ник переезжает в clients.telegram_username
-- (см. findOrCreateClient / resolveClient).

alter table bookings add column if not exists telegram_username text;
