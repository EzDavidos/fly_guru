-- 0020: убираем прямую запись инструктора в shifts.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0019).
--
-- Пачка правок №4, пак C — доработка по ревью безопасности.
--
-- Что было: в 0019 инструктору дали политики shifts_insert_own и
-- shifts_update_own, чтобы он открывал/закрывал СВОЮ смену. Но RLS не умеет
-- ограничивать НАБОР колонок: политика проверяет только instructor_id, поэтому
-- инструктор мог записать в свою строку что угодно — в том числе выставить
-- opened_at на 08:00 (казаться «вовремя», хотя открыл в 11:00) или planned=true
-- на незапланированном выходе (стереть ровно тот сигнал, ради которого флаг и
-- заведён). У инструктора есть валидный JWT в браузере, так что это делается
-- запросом к PostgREST мимо нашего интерфейса.
--
-- Что делаем: снимаем обе политики. Открытие/закрытие/создание смены теперь
-- идёт ТОЛЬКО через серверные экшены под service_role — метку времени ставит
-- сервер (new Date()), а не клиент, и подделать её нельзя. Роль и владельца
-- («instructor_id = я», «дата = сегодня») экшен проверяет в коде.
--
-- Что остаётся: shifts_select_staff (0014) — инструктор по-прежнему ВИДИТ смены
-- (календарь, экран смены); shift_photos_*_own (0019) — фото инструктор всё так
-- же грузит и удаляет сам, там подделывать нечего (только привязка своих фото к
-- своей смене).

drop policy if exists shifts_insert_own on shifts;
drop policy if exists shifts_update_own on shifts;
