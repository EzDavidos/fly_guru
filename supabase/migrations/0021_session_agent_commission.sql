-- 0021: комиссия агента, зафиксированная на сессии.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0020).
--
-- Пачка правок №4, пак D (пункт 2).
--
-- Что было: доля инструктора (15%) считалась со ВСЕГО чека сессии. Но по
-- договорённости с начальником инструктор получает 15% с чека МИНУС комиссия
-- агента: агент привёл клиента и забирает свои 300 000 ₫ «сверху», их из базы
-- инструктора надо вычесть. Пример: цена 2 млн → скидка 10% → чек 1.8 → агент
-- 300к → инструктор 15% от 1.5 млн = 225к (а не от 1.8).
--
-- Где взять размер комиссии позже: reward лежит в referral_rewards и привязан к
-- КЛИЕНТУ, а не к сессии. У клиента может быть несколько занятий — по одной
-- награде не понять, с какого чека её вычитать. Поэтому фиксируем комиссию на
-- самой сессии в момент записи: 0 для обычных, commission_fixed для агентских.
--
-- Marina 35% и CRM 2% этой колонки не касаются — они по-прежнему считаются от
-- полного чека (amount).

alter table sessions
  add column if not exists agent_commission bigint not null default 0;
