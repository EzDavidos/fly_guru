-- 0025: способ оплаты у абонемента.
-- Накатывается вручную через Supabase SQL Editor (как 0001–0024).
--
-- Пачка правок №6: в форме продажи абонемента был только флажок «оплата
-- получена» — ЧЕМ заплатили, нигде не хранилось. У сессий это поле есть с
-- 0016, поэтому в отчёте «чем платят» абонементы (6 млн ₫ за штуку!) зияли
-- дырой, а в заявке на абонемент способ оплаты приходилось выбирать руками.
--
-- Тот же справочник payment_methods, что у сессий, расходов и выплат агентам —
-- второй список форматов оплаты школе не нужен.
alter table subscriptions
  add column if not exists payment_method_id uuid references payment_methods(id);
